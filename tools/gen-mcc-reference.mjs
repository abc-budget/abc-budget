// MCC reference generator (Story 4.1 Task 5 — ENT-022).
//
// ┌──────────────────────────────────────────────────────────────────────────┐
// │  THIS IS A MANUAL GENERATOR. Run it BY HAND when you want to refresh the   │
// │  vendored MCC reference. It is NEVER wired into postinstall, prepare, or   │
// │  the build (NFR-005: standalone build + offline + supply-chain safety).    │
// │  The build and the runtime do ZERO network — the consumer imports the      │
// │  committed JSON directly.                                                   │
// └──────────────────────────────────────────────────────────────────────────┘
//
// Usage (from repo root):  npm run gen:mcc
//
// What it does:
//   - Fetches `Oleksios/Merchant-Category-Codes` at a PINNED commit SHA
//     (hardcoded below — supply-chain: we never float to the moving branch tip).
//   - Keeps uk + en only (DROPs ru).
//   - Zero-pads each code to "dddd" and flattens to
//       { "<dddd>": { "uk": "<title>", "en": "<title>" }, ... }
//   - Prepends a top-level "_meta" provenance block.
//   - Writes apps/web/src/app/mcc/mcc-reference.json (vendored, COMMITTED — it
//     lives WITH its consumer in apps/web, NOT in the engine, and is imported
//     as plain JSON, never `?raw`, never fetched).
//   - Writes apps/web/src/app/mcc/mcc-reference.LICENSE.md (MIT attribution).
//
// Source titles use the upstream `shortDescription` field.
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

// ── Pinned provenance ───────────────────────────────────────────────────────
const REPO = 'Oleksios/Merchant-Category-Codes';
const REPO_URL = `https://github.com/${REPO}`;
// Pinned commit on the default branch (main). Re-pin deliberately when refreshing.
const COMMIT_SHA = '322d99f576bc42c0248b6e83e8902c6cea5d23a6';
const LANGUAGES = ['uk', 'en'];

// The "Without groups" variant is a flat array of
//   { mcc, shortDescription, fullDescription }
// which is exactly what we want to flatten.
const RAW = (lang) =>
  `https://raw.githubusercontent.com/${REPO}/${COMMIT_SHA}/Without%20groups/mcc-${lang}.json`;

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = resolve(__dirname, '..', 'apps', 'web', 'src', 'app', 'mcc');
const OUT_JSON = resolve(OUT_DIR, 'mcc-reference.json');
const OUT_LICENSE = resolve(OUT_DIR, 'mcc-reference.LICENSE.md');

/** Zero-pads an MCC to a 4-digit "dddd" string. */
function pad4(code) {
  return String(code).trim().padStart(4, '0');
}

async function fetchLang(lang) {
  const url = RAW(lang);
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`fetch failed for ${lang}: HTTP ${res.status} ${url}`);
  }
  const rows = await res.json();
  if (!Array.isArray(rows)) {
    throw new Error(`expected an array from ${url}, got ${typeof rows}`);
  }
  /** @type {Map<string, string>} */
  const byCode = new Map();
  for (const row of rows) {
    const code = pad4(row.mcc);
    const title = String(row.shortDescription ?? row.fullDescription ?? '').trim();
    if (code.length === 4 && title) byCode.set(code, title);
  }
  return byCode;
}

async function main() {
  console.log(`[gen:mcc] MANUAL generator — fetching ${REPO} @ ${COMMIT_SHA}`);
  console.log('[gen:mcc] (this script is never run at install/build time)');

  const [uk, en] = await Promise.all([fetchLang('uk'), fetchLang('en')]);
  console.log(`[gen:mcc] uk codes: ${uk.size}, en codes: ${en.size}`);

  // Union of all codes seen in either language; sorted for a stable diff.
  const codes = [...new Set([...uk.keys(), ...en.keys()])].sort();

  /** @type {Record<string, unknown>} */
  const out = {
    _meta: {
      source: REPO_URL,
      commit: COMMIT_SHA,
      license: 'MIT',
      generated_by: 'tools/gen-mcc-reference.mjs',
      languages: LANGUAGES,
    },
  };
  for (const code of codes) {
    /** @type {Record<string, string>} */
    const entry = {};
    if (uk.has(code)) entry.uk = uk.get(code);
    if (en.has(code)) entry.en = en.get(code);
    out[code] = entry;
  }

  mkdirSync(OUT_DIR, { recursive: true });
  writeFileSync(OUT_JSON, JSON.stringify(out, null, 2) + '\n', 'utf8');
  writeFileSync(OUT_LICENSE, licenseDoc(), 'utf8');

  console.log(`[gen:mcc] wrote ${OUT_JSON} (${codes.length} codes)`);
  console.log(`[gen:mcc] wrote ${OUT_LICENSE}`);
}

